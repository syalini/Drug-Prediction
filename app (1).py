import streamlit as st
import joblib

model = joblib.load("final_pipeline_model.pkl")
le = joblib.load("label_encoder.pkl")

st.title("🩺 Drug Review Condition Classifier")

review = st.text_area("Enter Patient Review")

if st.button("Predict"):
    if review.strip():
        prediction = model.predict([review])
        condition = le.inverse_transform(prediction)[0]
        st.success(f"Predicted Condition: {condition}")
    else:
        st.warning("Please enter a review.")